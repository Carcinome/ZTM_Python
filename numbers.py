# Fundamental data types.

# type for asking what the data type is.

# integers:
print(type(2 + 4))
print(2 - 4)
print(2 / 4)
print(2 * 4)
print(2 % 4)

# For power of numbers, use **.
print(2 ** 4) # = 16

# Entire division, take the entire part of the division.
print(2 // 4) # = 0

# Modulo, remainder of the division. (remainder = reste)
print(2 % 4) # = 2

# float
print(type(2 / 4))
print(type(0.00002))
print(type(10 + 1.1))
print(type(9.9 + 1.1))

