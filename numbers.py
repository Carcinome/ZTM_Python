# round, for rounding the number. (rounding = arrondir)
print(round(3.1)) # = 3
print(round(3.8)) # = 4

# absolute, for absolute value of the argument (no negative number).
print(abs(-20)) # = 20
print(abs(-30.45)) # = 30.45